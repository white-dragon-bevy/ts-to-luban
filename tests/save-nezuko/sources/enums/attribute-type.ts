/**
 * 属性类型
 */
export enum AttributeType {
    /**
     * 生命值
     * @alias="health"
     */
    Health = 1,
    /**
     * 攻击力
     * @alias="attack"
     */
    Attack = 2,
    /**
     * 速度
     * @alias="speed"
     */
    Speed = 3,
    /**
     * 技能冷却缩减
     * @alias="cdReduction"
     */
    CdReduction = 4,
    /**
     * 暴击率
     * @alias="critical"
     */
    Critical = 5,
    /**
     * 攻击速度
     * @alias="attackSpeed"
     */
    AttackSpeed = 6,
    /**
     * 增伤
     * @alias="damageBonus"
     */
    DamageBonus = 7,
    /**
     * 幸运
     * @alias="luck"
     */
    Luck = 8,
    /**
     * 金币加成
     * @alias="coinBonus"
     */
    CoinBonus = 9,
}
